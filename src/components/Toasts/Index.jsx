import { toast } from "react-toastify";

export const ToastSuccess = (
    message = "Voce deve inserir sua mensagem como parametro da função"
) => {
    toast.success(message, {
        position: "bottom-right",
        autoClose: 3000,
        hideProgressBar: false,
        closeOnClick: true,
        pauseOnHover: true,
        draggable: true,
        progress: undefined,
        theme: "colored",
    });
};

